public class Horse {
	private String name;
	private String color;
	private String dob;
	
	String getName () {
		return name;
	}
	
	void setName (String name) {
		this.name = name;
	}
	
	String getColor () {
		return color;
	}
	
	void setColor (String color) {
		this.color = color;
	}
	
	String getDob () {
		return dob;
	}
	
	void setDob (String dob) {
		this.dob = dob;
	}
}